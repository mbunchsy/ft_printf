#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int nb2;

	if (n < 0)
	{
		write(fd, "-", 1);
		nb2 = -1 * n;
	}
	else
		nb2 = n;
	if (nb2 >= 10)
		ft_putnbr_fd(nb2 / 10, fd);
	nb2 = nb2 % 10 + 48;
	write(fd, &nb2, 1);
}
