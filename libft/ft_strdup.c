#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	char	*s2;
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (s1[i] != '\0')
		i++;
	if (!(s2 = (char *)malloc(i + 1)))
		return (NULL);
	while (*s1)
	{
		s2[j] = *s1++;
		j++;
	}
	s2[j] = '\0';
	return (s2);
}
